// Event Manager Class
class EventManager {
    constructor() {
        this.staticEvents = STATIC_EVENTS_DATA;
    }

    formatDate(date) {
        const year = date.getFullYear();
        const month = String(date.getMonth() + 1).padStart(2, '0');
        const day = String(date.getDate()).padStart(2, '0');
        return `${year}-${month}-${day}`;
    }

    // Get static events for a specific date
    getStaticEventsForDate(date) {
        const year = date.getFullYear();
        const dateKey = this.formatDate(date);
        
        if (this.staticEvents[year] && this.staticEvents[year][dateKey]) {
            return this.staticEvents[year][dateKey].map(event => ({
                ...event,
                isStatic: true
            }));
        }
        
        return [];
    }

    // Get all events for a month
    getAllStaticEventsForMonth(month, year) {
        const result = [];
        const daysInMonth = new Date(year, month + 1, 0).getDate();
        
        for (let day = 1; day <= daysInMonth; day++) {
            const date = new Date(year, month, day);
            const staticEvents = this.getStaticEventsForDate(date);
            
            staticEvents.forEach(event => {
                result.push({
                    date: this.formatDate(date),
                    data: event
                });
            });
        }
        
        return result;
    }

    // Get all static events for display
    getAllStaticEvents() {
        const result = [];
        
        for (const year in this.staticEvents) {
            for (const dateKey in this.staticEvents[year]) {
                this.staticEvents[year][dateKey].forEach(event => {
                    result.push({
                        date: dateKey,
                        data: { ...event, isStatic: true }
                    });
                });
            }
        }
        
        return result;
    }

    // Add new static event (for future use)
    addStaticEvent(year, dateKey, eventData) {
        if (!this.staticEvents[year]) {
            this.staticEvents[year] = {};
        }
        
        if (!this.staticEvents[year][dateKey]) {
            this.staticEvents[year][dateKey] = [];
        }
        
        this.staticEvents[year][dateKey].push({
            ...eventData,
            id: `static_${Date.now()}`
        });
    }

    // Get event type color
    getEventTypeColor(type) {
        const colors = {
            'GH': '#2ecc71', // Green
            'RH': '#e74c3c', // Red
            'ME': '#3498db', // Blue
            'BR': '#9b59b6', // Purple
            'OT': '#f39c12'  // Orange
        };
        return colors[type] || '#3498db';
    }

    // Get event type name
    getEventTypeName(type) {
        const names = {
            'GH': 'Global Holiday',
            'RH': 'Regional Holiday',
            'ME': 'Meeting',
            'BR': 'Birthday',
            'OT': 'Other'
        };
        return names[type] || 'Other';
    }
}

